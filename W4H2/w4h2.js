// UserManager sƒ±nƒ±fƒ±
class UserManager {
    constructor() {
        this.apiUrl = 'https://jsonplaceholder.typicode.com/users';
        this.storageKey = 'users_data';
        this.storageExpiryKey = 'users_data_expiry';
        this.container = app.querySelector('.user-container');
        this.users = [];
        this.init();
    }

    init() {
        this.loadUsers();
    }

    async loadUsers() {
        try {
            // √ñnce localStorage'dan veri kontrol√º
            const cachedData = this.getCachedData();
            if (cachedData) {
                this.users = cachedData;
                this.renderUsers();
                return;
            }

            this.showLoading();

            // Promise tabanlƒ± veri √ßekme
            const users = await this.fetchUsersWithPromise();
            this.users = users;
            this.saveToLocalStorage(users);
            this.renderUsers();
        } catch (error) {
            this.showError('Veri y√ºklenirken hata: ' + error.message);
        }
    }

    // Promise fonksiyonu ile hata y√∂netimi
    fetchUsersWithPromise() {
        return new Promise((resolve, reject) => {
            fetch(this.apiUrl)
                .then(response => {
                    // HTTP durum kontrol√º
                    if (!response.ok) {
                        throw new Error(`HTTP ${response.status}: ${response.statusText}`);
                    }
                    return response.json();
                })
                .then(data => {
                    // Veri kontrol√º
                    if (!data || !Array.isArray(data)) {
                        throw new Error('Ge√ßersiz veri formatƒ±');
                    }
                    if (data.length === 0) {
                        throw new Error('Kullanƒ±cƒ± verisi bulunamadƒ±');
                    }
                    resolve(data);
                })
                .catch(error => {
                    // Detaylƒ± hata mesajlarƒ±
                    let errorMessage = 'Bilinmeyen hata olu≈ütu';
                    
                    if (error.name === 'TypeError' && error.message.includes('fetch')) {
                        errorMessage = 'API\'ye baƒülanƒ±lamƒ±yor. ƒ∞nternet baƒülantƒ±nƒ±zƒ± kontrol edin.';
                    } else if (error.message.includes('HTTP 404')) {
                        errorMessage = 'API endpoint bulunamadƒ±.';
                    } else if (error.message.includes('HTTP 500')) {
                        errorMessage = 'Sunucu hatasƒ± olu≈ütu. L√ºtfen daha sonra tekrar deneyin.';
                    } else if (error.message.includes('HTTP 403')) {
                        errorMessage = 'API eri≈üimi reddedildi.';
                    } else if (error.message.includes('HTTP 429')) {
                        errorMessage = '√áok fazla istek g√∂nderildi. L√ºtfen bekleyin.';
                    } else if (error.message.includes('Ge√ßersiz veri formatƒ±')) {
                        errorMessage = 'API\'den gelen veri formatƒ± ge√ßersiz.';
                    } else if (error.message.includes('Kullanƒ±cƒ± verisi bulunamadƒ±')) {
                        errorMessage = 'API\'den kullanƒ±cƒ± verisi alƒ±namadƒ±.';
                    } else {
                        errorMessage = `Veri √ßekme hatasƒ±: ${error.message}`;
                    }
                    
                    console.error('Promise hatasƒ±:', error);
                    reject(new Error(errorMessage));
                });
        });
    }

    getCachedData() {
        try {
            const data = localStorage.getItem(this.storageKey);
            const expiry = localStorage.getItem(this.storageExpiryKey);

            if (data && expiry) {
                const expiryTime = parseInt(expiry, 10);
                if (Date.now() < expiryTime) {
                    return JSON.parse(data);
                }
            }
            return null;
        } catch (error) {
            console.error('localStorage okuma hatasƒ±:', error);
            return null;
        }
    }

    saveToLocalStorage(data) {
        try {
            const expiryTime = Date.now() + 24 * 60 * 60 * 1000; // 1 g√ºn sonra
            localStorage.setItem(this.storageKey, JSON.stringify(data));
            localStorage.setItem(this.storageExpiryKey, expiryTime.toString());
        } catch (error) {
            console.error('localStorage kaydetme hatasƒ±:', error);
        }
    }

    showLoading() {
        this.container.innerHTML = `
            <div class="loading">
                <h2>Kullanƒ±cƒ±lar y√ºkleniyor...</h2>
                <p>L√ºtfen bekleyiniz...</p>
            </div>
        `;
    }

    showError(message) {
        this.container.innerHTML = `
            <div class="error-message">
                <h2>Hata!</h2>
                <p>${message}</p>
                <button class="refresh-btn" onclick="userManager.loadUsers()">Tekrar Dene</button>
            </div>
        `;
    }

    renderUsers() {
        if (!this.users || this.users.length === 0) {
            this.container.innerHTML = '<div class="loading"><h3>Kullanƒ±cƒ± bulunamadƒ±</h3></div>';
            return;
        }

        const stats = this.calculateStats();
        
        this.container.innerHTML = `
            <div class="header">
                <h1>üë• Kullanƒ±cƒ± Y√∂netimi</h1>
                <p>Toplam ${this.users.length} kullanƒ±cƒ± bulundu</p>
            </div>
            
            <div class="stats">
                <div class="stat-item">
                    <div class="stat-number">${stats.total}</div>
                    <div class="stat-label">Toplam Kullanƒ±cƒ±</div>
                </div>
                <div class="stat-item">
                    <div class="stat-number">${stats.cities}</div>
                    <div class="stat-label">Farklƒ± ≈ûehir</div>
                </div>
                <div class="stat-item">
                    <div class="stat-number">${stats.companies}</div>
                    <div class="stat-label">Farklƒ± ≈ûirket</div>
                </div>
            </div>
            
            <button class="refresh-btn" onclick="userManager.refreshData()">
                üîÑ Verileri Yenile
            </button>
            
            <div class="users-grid">
                ${this.users.map(user => this.createUserCard(user)).join('')}
            </div>
        `;

        this.addDeleteListeners();
    }

    calculateStats() {
        const cities = new Set(this.users.map(user => user.address.city));
        const companies = new Set(this.users.map(user => user.company.name));
        
        return {
            total: this.users.length,
            cities: cities.size,
            companies: companies.size
        };
    }

    createUserCard(user) {
        return `
            <div class="user-card" data-user-id="${user.id}">
                <div class="user-header">
                    <h3 class="user-name">${user.name}</h3>
                    <button class="delete-btn" data-user-id="${user.id}">
                        üóëÔ∏è Sil
                    </button>
                </div>
                
                <div class="user-email">üìß ${user.email}</div>
                
                <div class="user-address">
                    <div class="address-title">üìç Adres Bilgileri</div>
                    <div class="address-details">
                        <strong>≈ûehir:</strong> ${user.address.city}<br>
                        <strong>Cadde:</strong> ${user.address.street}<br>
                        <strong>Suite:</strong> ${user.address.suite}<br>
                        <strong>Zipcode:</strong> ${user.address.zipcode}
                    </div>
                </div>
            </div>
        `;
    }

    addDeleteListeners() {
        const deleteButtons = document.querySelectorAll('.delete-btn');
        deleteButtons.forEach(button => {
            button.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                const userId = parseInt(button.getAttribute('data-user-id'));
                this.deleteUser(userId);
            });
        });
    }

    deleteUser(userId) {
        if (confirm('Bu kullanƒ±cƒ±yƒ± silmek istediƒüinizden emin misiniz?')) {
            // Array'den kullanƒ±cƒ±yƒ± kaldƒ±r
            this.users = this.users.filter(user => user.id !== userId);
            
            // localStorage'ƒ± g√ºncelle
            this.saveToLocalStorage(this.users);
            
            // Ekranƒ± yenile
            this.renderUsers();
            
            // Ba≈üarƒ± mesajƒ±
            alert('Kullanƒ±cƒ± ba≈üarƒ±yla silindi!');
            
            console.log(`Kullanƒ±cƒ± ID ${userId} silindi. Kalan kullanƒ±cƒ± sayƒ±sƒ±: ${this.users.length}`);
        }
    }

    async refreshData() {
        try {
            // localStorage'ƒ± temizle
            localStorage.removeItem(this.storageKey);
            localStorage.removeItem(this.storageExpiryKey);
            
            // Verileri yeniden y√ºkle
            await this.loadUsers();
            
            alert('Veriler ba≈üarƒ±yla yenilendi!');
        } catch (error) {
            this.showError('Veriler yenilenirken hata olu≈ütu: ' + error.message);
        }
    }
}

